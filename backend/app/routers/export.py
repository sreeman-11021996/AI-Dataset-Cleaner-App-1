def parse_operations(operation_type: str) -> list:
    """Parse the operation type string into individual operations"""
    if not operation_type:
        return []
    
    operations = []
    parts = operation_type.split(",")
    
    for part in parts:
        part = part.strip()
        if not part:
            continue
            
        if part == "remove_duplicates":
            operations.append({"type": "remove_duplicates"})
        elif part.startswith("fill_missing:"):
            ops = part.split(":")
            operations.append({
                "type": "fill_missing",
                "column": ops[1] if len(ops) > 1 else None,
                "strategy": ops[2] if len(ops) > 2 else "mean"
            })
        elif part.startswith("remove_outliers:"):
            ops = part.split(":")
            operations.append({
                "type": "remove_outliers",
                "column": ops[1] if len(ops) > 1 else None,
                "strategy": ops[2] if len(ops) > 2 else "iqr"
            })
        elif part.startswith("normalize:"):
            ops = part.split(":")
            operations.append({
                "type": "normalize",
                "strategy": ops[1] if len(ops) > 1 else "minmax"
            })
        elif part.startswith("encode_categorical:") or part.startswith("label_encode:"):
            ops = part.split(":")
            operations.append({
                "type": "encode_categorical",
                "column": ops[1] if len(ops) > 1 else None,
                "strategy": ops[2] if len(ops) > 2 else "onehot"
            })
        elif part.startswith("trim_whitespace:"):
            operations.append({
                "type": "trim_whitespace",
                "column": part.split(":")[1] if ":" in part else None
            })
        elif part.startswith("standardize_case:"):
            ops = part.split(":")
            operations.append({
                "type": "standardize_case",
                "column": ops[1] if len(ops) > 1 else None,
                "strategy": ops[2] if len(ops) > 2 else "lowercase"
            })
        elif part.startswith("text_cleaning:"):
            operations.append({
                "type": "text_cleaning",
                "column": part.split(":")[1] if ":" in part else None
            })
        elif part.startswith("drop_column:"):
            operations.append({
                "type": "drop_column",
                "column": part.split(":")[1] if ":" in part else None
            })
        elif part.startswith("auto_clean"):
            operations.append({"type": "auto_clean", "steps": parts[1:]})
    
    return operations


def get_column_types(df: pd.DataFrame) -> dict:
    """Get column types for the dataframe"""
    column_types = {}
    for col in df.columns:
        if pd.api.types.is_numeric_dtype(df[col]):
            column_types[col] = "numeric"
        elif pd.api.types.is_datetime64_any_dtype(df[col]):
            column_types[col] = "datetime"
        elif df[col].dtype == 'object':
            if df[col].nunique() <= 10:
                column_types[col] = "categorical"
            else:
                column_types[col] = "text"
        else:
            column_types[col] = "other"
    return column_types


def generate_pandas_script(operations: list, dataset_name: str, column_types: dict) -> str:
    """Generate a Python script using pandas"""
    
    lines = [
        "#!/usr/bin/env python3",
        "\"\"\"",
        f"ML Preprocessing Pipeline - {dataset_name}",
        "Generated by DatasetCleaner AI",
        "\"\"\"",
        "",
        "import pandas as pd",
        "import numpy as np",
        "from sklearn.preprocessing import (",
        "    StandardScaler, MinMaxScaler, LabelEncoder,",
        "    OneHotEncoder, RobustScaler",
        ")",
        "",
        "",
        "def load_data(filepath: str) -> pd.DataFrame:",
        '    """Load dataset from CSV file"""',
        "    df = pd.read_csv(filepath)",
        "    return df",
        "",
        "",
        "def preprocess_data(df: pd.DataFrame) -> pd.DataFrame:",
        '    """Apply cleaning and preprocessing steps"""',
        "    df = df.copy()",
        ""
    ]
    
    for op in operations:
        op_type = op.get("type")
        
        if op_type == "remove_duplicates":
            lines.append("    # Remove duplicate rows")
            lines.append("    df = df.drop_duplicates()")
            lines.append("")
        
        elif op_type == "fill_missing":
            col = op.get("column")
            strategy = op.get("strategy", "mean")
            if col and col in df.columns:
                lines.append(f"    # Fill missing values in '{col}' using {strategy}")
                if strategy == "mean" and column_types.get(col) == "numeric":
                    lines.append(f"    df['{col}'] = df['{col}'].fillna(df['{col}'].mean())")
                elif strategy == "median" and column_types.get(col) == "numeric":
                    lines.append(f"    df['{col}'] = df['{col}'].fillna(df['{col}'].median())")
                elif strategy == "mode":
                    lines.append(f"    df['{col}'] = df['{col}'].fillna(df['{col}'].mode()[0])")
                elif strategy == "forward_fill":
                    lines.append(f"    df['{col}'] = df['{col}'].fillna(method='ffill')")
                else:
                    lines.append(f"    df['{col}'] = df['{col}'].fillna('')")
                lines.append("")
        
        elif op_type == "remove_outliers":
            col = op.get("column")
            strategy = op.get("strategy", "iqr")
            if col and col in df.columns:
                lines.append(f"    # Remove outliers from '{col}' using {strategy}")
                if strategy == "iqr":
                    lines.append(f"    Q1 = df['{col}'].quantile(0.25)")
                    lines.append(f"    Q3 = df['{col}'].quantile(0.75)")
                    lines.append(f"    IQR = Q3 - Q1")
                    lines.append(f"    lower = Q1 - 1.5 * IQR")
                    lines.append(f"    upper = Q3 + 1.5 * IQR")
                    lines.append(f"    df = df[~((df['{col}'] < lower) | (df['{col}'] > upper))]")
                elif strategy == "zscore":
                    lines.append(f"    mean = df['{col}'].mean()")
                    lines.append(f"    std = df['{col}'].std()")
                    lines.append(f"    df = df[np.abs((df['{col}'] - mean) / std) <= 3]")
                lines.append("")
        
        elif op_type == "normalize":
            strategy = op.get("strategy", "minmax")
            lines.append(f"    # Normalize numeric columns using {strategy}")
            numeric_cols = [c for c, t in column_types.items() if t == "numeric"]
            if numeric_cols:
                for col in numeric_cols:
                    if strategy in ["minmax", "normalize"]:
                        lines.append(f"    min_val = df['{col}'].min()")
                        lines.append(f"    max_val = df['{col}'].max()")
                        lines.append(f"    if max_val > min_val:")
                        lines.append(f"        df['{col}'] = (df['{col}'] - min_val) / (max_val - min_val)")
                    elif strategy == "standardize":
                        lines.append(f"    mean = df['{col}'].mean()")
                        lines.append(f"    std = df['{col}'].std()")
                        lines.append(f"    if std > 0:")
                        lines.append(f"        df['{col}'] = (df['{col}'] - mean) / std")
                lines.append("")
        
        elif op_type == "encode_categorical":
            col = op.get("column")
            strategy = op.get("strategy", "onehot")
            if col and col in df.columns:
                lines.append(f"    # Encode '{col}' using {strategy}")
                if strategy == "onehot":
                    lines.append(f"    dummies = pd.get_dummies(df['{col}'], prefix='{col}', drop_first=True)")
                    lines.append(f"    df = pd.concat([df, dummies], axis=1)")
                    lines.append(f"    df = df.drop(columns=['{col}'])")
                else:
                    lines.append(f"    le = LabelEncoder()")
                    lines.append(f"    df['{col}'] = le.fit_transform(df['{col}'].astype(str))")
                lines.append("")
        
        elif op_type == "trim_whitespace":
            col = op.get("column")
            if col and col in df.columns:
                lines.append(f"    # Trim whitespace in '{col}'")
                lines.append(f"    df['{col}'] = df['{col}'].astype(str).str.strip()")
                lines.append("")
        
        elif op_type == "standardize_case":
            col = op.get("column")
            strategy = op.get("strategy", "lowercase")
            if col and col in df.columns:
                lines.append(f"    # Standardize case in '{col}' to {strategy}")
                lines.append(f"    df['{col}'] = df['{col}'].astype(str).str.{strategy}()")
                lines.append("")
        
        elif op_type == "text_cleaning":
            col = op.get("column")
            if col and col in df.columns:
                lines.append(f"    # Clean text in '{col}'")
                lines.append(f"    df['{col}'] = df['{col}'].astype(str).str.lower()")
                lines.append(f"    df['{col}'] = df['{col}'].str.replace(r'[^\\w\\s]', '', regex=True)")
                lines.append(f"    df['{col}'] = df['{col}'].str.replace(r'\\s+', ' ', regex=True).str.strip()")
                lines.append("")
        
        elif op_type == "drop_column":
            col = op.get("column")
            if col and col in df.columns:
                lines.append(f"    # Drop column '{col}'")
                lines.append(f"    df = df.drop(columns=['{col}'])")
                lines.append("")
        
        elif op_type == "auto_clean":
            lines.append("    # Auto-clean: Remove duplicates")
            lines.append("    df = df.drop_duplicates()")
            lines.append("")
            lines.append("    # Auto-clean: Fill missing values")
            lines.append("    for col in df.columns:")
            lines.append("        if df[col].isnull().sum() > 0:")
            lines.append("            if pd.api.types.is_numeric_dtype(df[col]):")
            lines.append("                df[col] = df[col].fillna(df[col].median())")
            lines.append("            else:")
            lines.append("                df[col] = df[col].fillna(df[col].mode()[0] if len(df[col].mode()) > 0 else '')")
            lines.append("")
            lines.append("    # Auto-clean: Normalize numeric columns")
            lines.append("    numeric_cols = df.select_dtypes(include=[np.number]).columns")
            lines.append("    for col in numeric_cols:")
            lines.append("        min_val = df[col].min()")
            lines.append("        max_val = df[col].max()")
            lines.append("        if max_val > min_val:")
            lines.append("            df[col] = (df[col] - min_val) / (max_val - min_val)")
            lines.append("")
    
    lines.append("    return df")
    lines.append("")
    lines.append("")
    lines.append("if __name__ == '__main__':")
    lines.append(f"    # Load and preprocess the dataset")
    lines.append(f"    df = load_data('{dataset_name}.csv')")
    lines.append(f"    df_clean = preprocess_data(df)")
    lines.append(f"    ")
    lines.append(f"    # Save the cleaned dataset")
    lines.append(f"    df_clean.to_csv('{dataset_name}_cleaned.csv', index=False)")
    lines.append(f"    print('Preprocessing complete!')")
    
    return "\n".join(lines)


def generate_sklearn_pipeline(operations: list, dataset_name: str, column_types: dict) -> str:
    """Generate a scikit-learn pipeline"""
    
    numeric_cols = [c for c, t in column_types.items() if t == "numeric"]
    categorical_cols = [c for c, t in column_types.items() if t == "categorical"]
    
    lines = [
        "#!/usr/bin/env python3",
        "\"\"\"",
        f"ML Preprocessing Pipeline (scikit-learn) - {dataset_name}",
        "Generated by DatasetCleaner AI",
        "\"\"\"",
        "",
        "import pandas as pd",
        "import numpy as np",
        "from sklearn.pipeline import Pipeline, FeatureUnion",
        "from sklearn.compose import ColumnTransformer",
        "from sklearn.preprocessing import (",
        "    StandardScaler, MinMaxScaler, LabelEncoder,",
        "    OneHotEncoder, RobustScaler, FunctionTransformer",
        ")",
        "from sklearn.base import BaseEstimator, TransformerMixin",
        "import joblib",
        "",
        "",
        "# Custom transformer for selecting columns",
        "class ColumnSelector(BaseEstimator, TransformerMixin):",
        "    def __init__(self, columns):",
        "        self.columns = columns",
        "    ",
        "    def fit(self, X, y=None):",
        "        return self",
        "    ",
        "    def transform(self, X):",
        "        return X[self.columns].values",
        "",
        "",
        "# Custom transformer for removing duplicates",
        "class DuplicateRemover(BaseEstimator, TransformerMixin):",
        "    def __init__(self):",
        "        pass",
        "    ",
        "    def fit(self, X, y=None):",
        "        return self",
        "    ",
        "    def transform(self, X):",
        "        if isinstance(X, np.ndarray):",
        "            X = pd.DataFrame(X)",
        "        return X.drop_duplicates().values",
        "",
        "",
        "# Custom transformer for filling missing values",
        "class MissingValueImputer(BaseEstimator, TransformerMixin):",
        "    def __init__(self, strategy='median'):",
        "        self.strategy = strategy",
        "    ",
        "    def fit(self, X, y=None):",
        "        if isinstance(X, np.ndarray):",
        "            X = pd.DataFrame(X)",
        "        if self.strategy in ['mean', 'median']:",
        "            self.fill_value_ = X.mean() if self.strategy == 'mean' else X.median()",
        "        else:",
        "            self.fill_value_ = X.mode().iloc[0] if len(X.mode()) > 0 else ''",
        "        return self",
        "    ",
        "    def transform(self, X):",
        "        if isinstance(X, np.ndarray):",
        "            X = pd.DataFrame(X)",
        "        return X.fillna(self.fill_value_).values",
        "",
        "",
        "def create_preprocessing_pipeline():",
        '    """Create the scikit-learn preprocessing pipeline"""',
        "",
    ]
    
    has_numeric = len(numeric_cols) > 0
    has_categorical = len(categorical_cols) > 0
    
    if has_numeric:
        lines.append(f"    # Numeric columns: {numeric_cols}")
        lines.append(f"    numeric_transformer = Pipeline(steps=[")
        lines.append(f"        ('imputer', MissingValueImputer(strategy='median')),")
        
        normalize_op = next((op for op in operations if op.get("type") == "normalize"), None)
        if normalize_op and normalize_op.get("strategy") == "standardize":
            lines.append(f"        ('scaler', StandardScaler())")
        elif normalize_op and normalize_op.get("strategy") == "robust":
            lines.append(f"        ('scaler', RobustScaler())")
        else:
            lines.append(f"        ('scaler', MinMaxScaler())")
        
        lines.append(f"    ])")
        lines.append("")
    
    if has_categorical:
        lines.append(f"    # Categorical columns: {categorical_cols}")
        lines.append(f"    categorical_transformer = Pipeline(steps=[")
        lines.append(f"        ('imputer', MissingValueImputer(strategy='most_frequent')),")
        
        encode_op = next((op for op in operations if op.get("type") == "encode_categorical"), None)
        if encode_op and encode_op.get("strategy") == "label_encode":
            lines.append(f"        ('encoder', LabelEncoder())")
        else:
            lines.append(f"        ('encoder', OneHotEncoder(handle_unknown='ignore', drop='first'))")
        
        lines.append(f"    ])")
        lines.append("")
    
    if has_numeric and has_categorical:
        lines.append(f"    # Combine numeric and categorical transformers")
        lines.append(f"    preprocessor = ColumnTransformer(")
        lines.append(f"        transformers=[")
        lines.append(f"            ('num', numeric_transformer, {json.dumps(numeric_cols)}),")
        lines.append(f"            ('cat', categorical_transformer, {json.dumps(categorical_cols)})")
        lines.append(f"        ])")
        lines.append(f"    )")
    elif has_numeric:
        lines.append(f"    preprocessor = numeric_transformer")
    elif has_categorical:
        lines.append(f"    preprocessor = categorical_transformer")
    else:
        lines.append(f"    # No preprocessing needed")
        lines.append(f"    preprocessor = None")
    
    lines.append("")
    lines.append(f"    # Create the full pipeline")
    lines.append(f"    pipeline = Pipeline(steps=[")
    lines.append(f"        ('preprocessor', preprocessor) if preprocessor else ('passthrough', FunctionTransformer())")
    lines.append(f"    ])")
    lines.append("")
    lines.append(f"    return pipeline")
    lines.append("")
    lines.append("")
    lines.append("def preprocess_and_save(input_path: str, output_path: str):")
    '    """Load data, apply pipeline, and save the result"""'
    lines.append(f"    # Load data")
    lines.append(f"    df = pd.read_csv(input_path)")
    lines.append(f"    ")
    lines.append(f"    # Create and fit the pipeline")
    lines.append(f"    pipeline = create_preprocessing_pipeline()")
    lines.append(f"    X = df.values")
    lines.append(f"    X_transformed = pipeline.fit_transform(X)")
    lines.append(f"    ")
    lines.append(f"    # Convert back to DataFrame")
    lines.append(f"    df_clean = pd.DataFrame(X_transformed)")
    lines.append(f"    ")
    lines.append(f"    # Save the cleaned data")
    lines.append(f"    df_clean.to_csv(output_path, index=False)")
    lines.append(f"    ")
    lines.append(f"    # Save the fitted pipeline for future use")
    lines.append(f"    joblib.dump(pipeline, 'preprocessing_pipeline.joblib')")
    lines.append(f"    ")
    lines.append(f"    return pipeline")
    lines.append("")
    lines.append("")
    lines.append("if __name__ == '__main__':")
    lines.append(f"    pipeline = preprocess_and_save('{dataset_name}.csv', '{dataset_name}_cleaned.csv')")
    lines.append(f"    print('Pipeline created and data preprocessed successfully!')")
    lines.append(f"    print(f'Pipeline steps: {{pipeline.named_steps}}')")
    
    return "\n".join(lines)


