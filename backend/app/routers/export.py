@router.get("/{dataset_id}/export/script")
async def export_python_script(
    dataset_id: uuid.UUID,
    db: Session = Depends(get_db),
    current_user: User = Depends(get_current_user)
):
    """Export preprocessing pipeline as a Python script"""
    
    dataset = db.query(DatasetModel).filter(
        DatasetModel.id == dataset_id,
        DatasetModel.user_id == current_user.id
    ).first()
    
    if not dataset:
        raise HTTPException(status_code=404, detail="Dataset not found")
    
    operations = db.query(CleaningOperation).filter(
        CleaningOperation.dataset_id == dataset_id
    ).all()
    
